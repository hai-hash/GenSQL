    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if ((!xlsxSelect.isSelected() && !textSelect.isSelected()) || chooseFileText.getText().isEmpty() || nameDatabase.getText().isEmpty() || nameTable.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Please input full data in the screen");
            } else {
                String nameFile = fileSelect.getName();
                String[] nameSplit = nameFile.split("[.]");
                System.out.println("name split" + nameSplit);
                String typeFile = nameSplit[nameSplit.length-1].trim().toLowerCase();
                if (xlsxSelect.isSelected()) {
                    if(typeFile.equals("xlsx")){
                    GeneralDataInsert.generalDataQueryInsertWithExcell(nameDatabase.getText(), nameTable.getText());
                    JOptionPane.showMessageDialog(rootPane, "General success !");
                    }
                    else{
                        JOptionPane.showMessageDialog(rootPane, "File have type different with format your selected");
                    }
                    
                } else {
                    JOptionPane.showMessageDialog(rootPane, "This Function is not developed");
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

