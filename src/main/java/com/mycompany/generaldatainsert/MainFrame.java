    private void btnChooseFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChooseFileActionPerformed
        JFileChooser chooseFile = new JFileChooser(FileSystemView
                .getFileSystemView()
                .getHomeDirectory());
        int res = chooseFile.showOpenDialog(null);

        if (res == JFileChooser.APPROVE_OPTION) {
            fileSelect = chooseFile.getSelectedFile();
            String pathFile = fileSelect.getAbsolutePath();
            chooseFileText.setText(pathFile);
        }
    }//GEN-LAST:event_btnChooseFileActionPerformed

